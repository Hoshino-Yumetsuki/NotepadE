fn check_page(
    name: &str,
    hand: &[u16; 128],
    dec: &[char; 128],
    enc: &oem_cp::OEMCPHashMap<char, u8>,
) {
    // decode: every high byte maps to the same scalar
    for i in 0..128usize {
        assert_eq!(
            dec[i] as u32,
            hand[i] as u32,
            "{name} decode byte 0x{:02X}",
            i + 0x80
        );
    }
    // encode: exhaustive BMP scan must agree with the hand reverse lookup
    for v in 0u32..=0xFFFF {
        let Some(c) = char::from_u32(v) else { continue };
        let hand_byte: u8 = if v < 0x80 {
            v as u8
        } else {
            hand.iter()
                .position(|&u| u as u32 == v)
                .map(|idx| (idx as u8) + 0x80)
                .unwrap_or(b'?')
        };
        let crate_byte = oem_cp::encode_char_lossy(c, enc);
        assert_eq!(
            crate_byte, hand_byte,
            "{name} encode U+{v:04X}: crate 0x{crate_byte:02X} vs hand 0x{hand_byte:02X}"
        );
    }
    // astral chars -> '?'
    assert_eq!(
        oem_cp::encode_char_lossy('\u{1F600}', enc),
        b'?',
        "{name} astral"
    );
}

